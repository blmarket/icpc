typedef pair<int,int> PII;

template<typename T> int size(const T &a) { return a.size(); } 

int n;
vector<PII> ps;
int life[110000];

void process(void)
{
    cin >> n;
    ps.clear();
    for(int i=0;i<n;i++)
    {
        int p,s;
        cin >> p >> s;
        ps.pb(mp(p,s));
    }
    memset(life, 0, sizeof(life));
}

int main(void)
{
    int N;
    cin >> N;
    for(int i=1;i<=N;i++)
    {
        printf("Case #%d: ",i);
        process();
        cerr << i << endl;
    }
}
