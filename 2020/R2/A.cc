LL L, R;

void process() {
  scanf(" %lld %lld", &L, &R);
  for(LL i=1;;i++) {
    if(L >= R && L >= i) {
      L -= i;
    } else if (R >= i) {
      R -= i;
    } else {
      cout << i << " " << L << " " << R << endl;
      return;
    }
  }
}

int main(void) {
    int T;
    scanf(" %d", &T);
    for(int i=1;i<=T;i++) {
        printf("Case #%d: ", i);
        process();
    }
    return 0;
}
