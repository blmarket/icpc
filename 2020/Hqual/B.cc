int N;
char str[100005];

void process() {
  scanf(" %d", &N);
  scanf(" %s", str);
  int c[2] = {0,0};
  for(int i=0;i<N;i++) c[str[i]-'A']++;
  if(abs(c[0] - c[1]) == 1) {
    cout << 'Y' << endl;
  } else {
    cout << 'N' << endl;
  }
}

