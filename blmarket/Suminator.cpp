int check(vector<int> prog)
{
    vector<int> stack;
    for(int i=0;i<size(prog);i++) {
        if(prog[i] == 0) {
            if(stack.size() < 2) continue;

            int tmp = stack.back();
            stack.pop_back();
            stack.back() += tmp;
        } else {
            stack.pb(prog[i]);
        }
    }
    return stack.back();
}

