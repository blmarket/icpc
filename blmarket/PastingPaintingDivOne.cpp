void paste(int a) {
    int n = size(board);
    int m = size(board[0]);
    for(int i=0;i<n;i++) {
        for(int j=0;j<m;j++) {
            if(board[i][j] != '.') tempboard[i+a][j+a] = board[i][j];
        }
    }
}

void cnt(LL &R, LL &G, LL &B) {
    R=G=B=0;
    for(int i=0;i<200;i++) {
        for(int j=0;j<200;j++) {
            if(tempboard[i][j] == 'R') R++;
            else if(tempboard[i][j] == 'G') G++;
            else if(tempboard[i][j] == 'B') B++;
        }
    }
}

